package com.ruhuo.xuaizerobackend.ratelimiter.enums;

/**
 * 限流类型枚举类
 * 定义了系统中不同层级的限流类型，用于区分不同场景下的限流策略
 */
public enum RateLimitType {
    /**
     * 接口级别限流
     * 针对特定API接口进行限流控制，限制接口的调用频率
     * 适用于需要对单个接口进行精确流量控制的场景
     */
    API,

    /**
     * 用户级别限流
     * 针对单个用户进行限流控制，限制同一用户在单位时间内的请求次数
     * 适用于需要防止用户恶意请求或保护系统免受单个用户过度使用的场景
     */
    USER,

    /**
     * IP级别限流
     * 针对客户端IP地址进行限流控制，限制同一IP在单位时间内的请求次数
     * 适用于需要防止恶意IP攻击或保护系统免受特定IP过度访问的场景
     */
    IP
}
